function getPropertyValue(propName, propConfig, formData) {
    const { type } = propConfig;
    const value = formData.get ? formData.get(propName) : formData[propName];

    if (!value && type !== 'checkbox') return null;

    switch (type) {
        case 'title':
            return {
                title: [{
                    text: {
                        content: value
                    }
                }]
            };

        case 'rich_text':
            return {
                rich_text: [{
                    text: {
                        content: value
                    }
                }]
            };

        case 'number':
            return {
                number: parseFloat(value) || null
            };

        case 'select':
            return value ? {
                select: {
                    name: value
                }
            } : null;

        case 'multi_select':
            const tags = value.split(',').map(tag => tag.trim()).filter(tag => tag);
            return tags.length > 0 ? {
                multi_select: tags.map(tag => ({ name: tag }))
            } : null;

        case 'date':
            return value ? {
                date: {
                    start: value
                }
            } : null;

        case 'checkbox':
            return {
                checkbox: formData.hasOwnProperty ? formData.hasOwnProperty(propName) : formData.has(propName)
            };

        case 'url':
            return value ? {
                url: value
            } : null;

        case 'email':
            return value ? {
                email: value
            } : null;

        case 'phone_number':
            return value ? {
                phone_number: value
            } : null;

        default:
            return value ? {
                rich_text: [{
                    text: {
                        content: value
                    }
                }]
            } : null;
    }
}

