// Notion API Helper with local proxy
class NotionAPI {
    constructor(token) {
        this.token = token;
        // Î°úÏª¨ ÌîÑÎ°ùÏãú ÏÑúÎ≤Ñ ÏÇ¨Ïö© (CORS ÏôÑÏ†Ñ Ìï¥Í≤∞!)
        this.baseURL = window.location.origin + '/api/notion';
        this.headers = {
            'Authorization': `Bearer ${token}`,
            'Notion-Version': '2022-06-28',
            'Content-Type': 'application/json'
        };
    }

    async request(endpoint, options = {}) {
        const url = `${this.baseURL}${endpoint}`;
        
        try {
            const response = await fetch(url, {
                ...options,
                headers: {
                    ...this.headers,
                    ...options.headers
                }
            });

            if (!response.ok) {
                const error = await response.json().catch(() => ({ message: `HTTP ${response.status}` }));
                throw new Error(error.message || `HTTP ${response.status}`);
            }

            return response.json();
        } catch (error) {
            console.error('API Error:', error);
            throw error;
        }
    }

    async searchDatabases() {
        return this.request('/search', {
            method: 'POST',
            body: JSON.stringify({
                filter: {
                    value: 'database',
                    property: 'object'
                }
            })
        });
    }

    async getDatabase(databaseId) {
        return this.request(`/databases/${databaseId}`);
    }

    async createPage(databaseId, properties, children = []) {
        return this.request('/pages', {
            method: 'POST',
            body: JSON.stringify({
                parent: {
                    database_id: databaseId
                },
                properties,
                children
            })
        });
    }
}

